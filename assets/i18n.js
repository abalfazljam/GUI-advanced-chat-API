window.t = function(key, params={}) {
  const lang = window.currentLang || localStorage.getItem('chat_lang') || 'fa';
  let txt = (window.I18N_DICT[lang] && window.I18N_DICT[lang][key]) || (window.I18N_DICT['fa'][key]) || key;
  for (let k in params) {
    txt = txt.replace(`{${k}}`, params[k]);
  }
  return txt;
};

window.applyI18n = function(lang) {
  window.currentLang = lang;
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'fa' ? 'rtl' : 'ltr';
  document.documentElement.setAttribute('data-lang', lang);
  document.cookie = `chat_lang=${lang}; path=/; max-age=31536000`;
  localStorage.setItem('chat_lang', lang);
  // Update all [data-i18n]
  document.querySelectorAll('[data-i18n]').forEach(el=>{
    const key = el.getAttribute('data-i18n');
    if (!key) return;
    // if element has HTML allowed (version_info contains <br>)
    const val = window.t(key);
    if (val.includes('<br>') || val.includes('❤️')) {
      el.innerHTML = val;
    } else {
      el.textContent = val;
    }
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el=>{
    const key = el.getAttribute('data-i18n-placeholder');
    el.placeholder = window.t(key);
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el=>{
    const key = el.getAttribute('data-i18n-title');
    el.title = window.t(key);
  });
  // Update page title
  document.title = window.t('page_title');
  // Update lang select
  const sel = document.getElementById('langSelect');
  if (sel) sel.value = lang;
};

document.addEventListener('DOMContentLoaded', ()=>{
  let lang = localStorage.getItem('chat_lang');
  if (!lang) {
    // try cookie
    const m = document.cookie.match(/chat_lang=([^;]+)/);
    if (m) lang = m[1];
  }
  if (!lang) lang = 'fa';
  if (!['fa','en'].includes(lang)) lang='fa';
  window.applyI18n(lang);
});

